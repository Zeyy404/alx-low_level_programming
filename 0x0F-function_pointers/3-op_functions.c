#include <stdio.h>

/**
 * op_add - calculates the sum of two intgers
 * @a: first integer
 * @b: second integer
 * Return: int sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - calculates the difference between two integers
 * @a: first integer
 * @b: second integer
 * Return: int difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - calculates the product of two integers
 * @a: first integer
 * @b: second integer
 * Return: int product
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - calculates the result of division between two integers
 * @a: first integer
 * @b: second integer
 * Return: int result
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calculates the reminder of division of two integers
 * @a: first integer
 * @b: second integer
 * Return: int reminder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
